const num = +window.prompt("정수 입력:")
if (num > 0) {
    window.console.log(`${num}은(는) 자연수입니다.`)
    window.console.log("우뿌뿌뿌뿌뿌")
}
else if (num == 0) {
    window.console.log(`${num}은 0 입니다.`)
}
else {
    window.console.log(`${num}은 0 또는 음수입니다.`)
}
window.console.log("프로그램 끄읏")